            <h2> Project Under Look Process </h2>
            <p> Academic project management is a major issue in India as most of the colleges don't have automated platform which can keep the records of the academic projects submitted by the students. Also its a very complicated and time taking process to assign project mentors to the students and handling the weekly feedback and report process. After observing these academic project management challenges in our college we had decided to built a platform where students , project mentors and HOD all can together handle and manage this process together at one place. </p>
             <br><p>The prime objective of P.U.L.P is to reduce the time wastage in project handling and reduce extra burdens required for project management in the college.

Generally we see that in many colleges different whatsapp groups are made where peoples submit there synopsis to project mentors and lots of different groups are created .

Also this process demands for an extra effort to note down the names of students who had submitted there projects to keep records which makes it complex to handle.

This platform makes it easy it create record by default along with the name of the project mentors and the project documents.</p>
